    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        if(btnLogin_Username.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Tai Khoan ko dc de trong", "LOI", JOptionPane.ERROR_MESSAGE);
        }else if(String.valueOf(pwd.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null, "Mat Khau ko dc de trong", "LOI", JOptionPane.ERROR_MESSAGE);
        }else{
            NhanVienDAO nvDAO = new NhanVienDAO();
            List<NhanVien> nv = nvDAO.getNhanVienWithCrit(btnLogin_Username.getText(), String.valueOf(pwd.getPassword()));
            if(!nv.isEmpty()){
//                if(!nv.get(0).getTendangnhap().equals(btnLogin_Username.getText())){
//                    JOptionPane.showMessageDialog(null, "Sai Tai Khoan", "LOI", JOptionPane.ERROR_MESSAGE);
//                }else if(!nv.get(0).getTendangnhap().equals(String.valueOf(pwd.getPassword()))){
//                    JOptionPane.showMessageDialog(null, "Sai Mat Khau", "LOI", JOptionPane.ERROR_MESSAGE);
//                }else{
//                    new giaoDIen().setVisible(true);
//                }
            if(nv.get(0).getCapbac().equalsIgnoreCase("quan ly") || nv.get(0).getCapbac().equalsIgnoreCase("boss")){
                       giaoDIen gd = new giaoDIen();
                       gd.showFormThongTin(nv.get(0));
                        gd.setVisible(true);
                        this.setVisible(false);
            }else{
                       giaoDIen gd = new giaoDIen();
                       gd.showFormThongTin(nv.get(0));
                        gd.setVisible(true);
                        this.setVisible(false);
                        gd.hideNV();
            }
            }else{
                    JOptionPane.showMessageDialog(null, "Tai Khoan khong ton tai", "LOI", JOptionPane.ERROR_MESSAGE);
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

